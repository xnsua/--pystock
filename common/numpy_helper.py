import numpy as np
def np_shift(arr, num, fill_value=np.nan):
    assert len(arr.shape) == 1
    result = np.empty_like(arr)
    if num > 0:
        result[:num] = fill_value
        result[num:] = arr[:-num]
    elif num < 0:
        result[num:] = fill_value
        result[:num] = arr[-num:]
    else:
        result = arr
    return result



